package nl.rivm.cib.episim.model;

import io.coala.time.x.Duration;
import io.coala.time.x.Instant;

/**
 * {@link ContactEvent} generated by some {@link Place} represents the interval
 * ({@link #getStart()} and {@link #getDuration()}) at some {@link Place} when a
 * primary {@link Carrier} (e.g. carrying some {@link Infection} at infective
 * {@link EpidemicCompartment}) shared some transmission
 * {@link TransmissionRoute} with some secondary (possibly) susceptible
 * {@link Carrier}
 * 
 * @version $Id: 90c08638280569764bd6c0b9746157f787d8c3cc $
 * @author Rick van Krevelen
 */
public class ContactEvent
{

	private Instant start;

	private Duration duration;

	private TransmissionSpace space;

	private TransmissionRoute route;

	private Condition primary;

	private Condition secondary;

	public ContactEvent( final Instant start, final Duration duration,
		final TransmissionSpace space, final TransmissionRoute route,
		final Condition primary, final Condition secondary )
	{
		this.start = start;
		this.duration = duration;
		this.space = space;
		this.route = route;
		this.primary = primary;
		this.secondary = secondary;
	}

	/** @return the contact onset {@link Instant} */
	public Instant getStart()
	{
		return this.start;
	}

	/** @return the contact {@link Duration} */
	public Duration getDuration()
	{
		return this.duration;
	}

	/** @return the contact {@link TransmissionSpace} */
	public TransmissionSpace getSpace()
	{
		return this.space;
	}

	/** @return the contact transmission {@link TransmissionRoute} */
	public TransmissionRoute getTransmissionRoute()
	{
		return this.route;
	}

	/**
	 * @return the {@link Condition} of the primary (infective) {@link Carrier}
	 */
	public Condition getPrimaryCondition()
	{
		return this.primary;
	}

	/**
	 * @return the {@link Condition} of the secondary (susceptible)
	 *         {@link Carrier}
	 */
	public Condition getSecondaryCondition()
	{
		return this.secondary;
	}

}